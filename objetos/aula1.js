//Estrutura básica de um objeto

const vitor = {
    nome: 'Vitor J Guerra',
    idade: 25
};

console.log (vitor.nome);
console.log (vitor.idade);
console.log (vitor);

    /*
    Explicações:

    Criação do Objeto vitor: Um objeto chamado vitor é criado com duas propriedades, nome e idade.

    Imprimindo a Propriedade nome: console.log(vitor.nome); imprime no console o valor da propriedade nome do objeto vitor. No caso, imprimirá "Vitor J Guerra".

    Imprimindo a Propriedade idade: console.log(vitor.idade); imprime no console o valor da propriedade idade do objeto vitor. No caso, imprimirá "25".

    Imprimindo o Objeto Completo: console.log(vitor); imprime no console o objeto vitor completo. A saída mostrará algo semelhante a { nome: 'Vitor J Guerra', idade: 25 }.
    */